package pluralsight.chapter1;

public class Flight {
	private int passengers;
	private int seats;
	
	
	public int getPassengers() {
		return passengers;
	}
	public void setPassengers(int passengers) {
		this.passengers = passengers;
	}
	public int getSeats() {
		return seats;
	}
	public void setSeats(int seats) {
		this.seats= seats;
	}
	
	void add1Passengers() {
		if (passengers < seats);
		passengers += 1;
	}

}
	